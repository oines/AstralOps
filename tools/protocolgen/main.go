package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"

	protocolmeta "github.com/oines/astralops/pkg/protocol"
)

type typeDecl struct {
	name string
	expr ast.Expr
}

type enumValue struct {
	name  string
	value string
}

type generator struct {
	types       []typeDecl
	stringEnums map[string][]enumValue
	actionSpecs []actionSpec
	eventFields map[string][]string
}

type actionSpec struct {
	action     string
	paramsType string
	resultType string
}

func main() {
	pkgDir := flag.String("pkg", "", "Go package directory to read")
	out := flag.String("out", "", "TypeScript output path")
	swiftOut := flag.String("swift-out", "", "Swift output path")
	flag.Parse()
	if *pkgDir == "" || *out == "" {
		fatalf("-pkg and -out are required")
	}
	gen, err := load(*pkgDir)
	if err != nil {
		fatalf("%v", err)
	}
	body, err := gen.emit()
	if err != nil {
		fatalf("%v", err)
	}
	if err := os.MkdirAll(filepath.Dir(*out), 0o755); err != nil {
		fatalf("%v", err)
	}
	if err := os.WriteFile(*out, body, 0o644); err != nil {
		fatalf("%v", err)
	}
	if *swiftOut != "" {
		swiftBody, err := gen.emitSwift()
		if err != nil {
			fatalf("%v", err)
		}
		if err := os.MkdirAll(filepath.Dir(*swiftOut), 0o755); err != nil {
			fatalf("%v", err)
		}
		if err := os.WriteFile(*swiftOut, swiftBody, 0o644); err != nil {
			fatalf("%v", err)
		}
	}
}

func load(pkgDir string) (*generator, error) {
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, pkgDir, func(info os.FileInfo) bool {
		return !strings.HasSuffix(info.Name(), "_test.go")
	}, 0)
	if err != nil {
		return nil, err
	}
	var pkg *ast.Package
	for _, value := range pkgs {
		pkg = value
		break
	}
	if pkg == nil {
		return nil, fmt.Errorf("no Go package found in %s", pkgDir)
	}
	gen := &generator{stringEnums: map[string][]enumValue{}}
	for _, file := range pkg.Files {
		for _, decl := range file.Decls {
			genDecl, ok := decl.(*ast.GenDecl)
			if !ok {
				continue
			}
			switch genDecl.Tok {
			case token.TYPE:
				for _, spec := range genDecl.Specs {
					typeSpec, ok := spec.(*ast.TypeSpec)
					if !ok || !typeSpec.Name.IsExported() {
						continue
					}
					if typeSpec.Assign.IsValid() {
						if _, external := typeSpec.Type.(*ast.SelectorExpr); external {
							continue
						}
						gen.types = append(gen.types, typeDecl{name: typeSpec.Name.Name, expr: typeSpec.Type})
						continue
					}
					gen.types = append(gen.types, typeDecl{name: typeSpec.Name.Name, expr: typeSpec.Type})
				}
			case token.CONST:
				var blockType string
				for _, spec := range genDecl.Specs {
					valueSpec, ok := spec.(*ast.ValueSpec)
					if !ok {
						continue
					}
					if valueSpec.Type != nil {
						if typeName, ok := valueSpec.Type.(*ast.Ident); ok && typeName.IsExported() {
							blockType = typeName.Name
						} else {
							blockType = ""
						}
					}
					if blockType == "" || len(valueSpec.Values) == 0 {
						continue
					}
					for index, name := range valueSpec.Names {
						if !name.IsExported() || index >= len(valueSpec.Values) {
							continue
						}
						lit, ok := valueSpec.Values[index].(*ast.BasicLit)
						if !ok || lit.Kind != token.STRING {
							continue
						}
						value, err := strconv.Unquote(lit.Value)
						if err != nil {
							return nil, err
						}
						gen.stringEnums[blockType] = append(gen.stringEnums[blockType], enumValue{name: name.Name, value: value})
					}
				}
			case token.VAR:
				specs, err := parseControlActionSpecs(genDecl)
				if err != nil {
					return nil, err
				}
				gen.actionSpecs = append(gen.actionSpecs, specs...)
			}
		}
	}
	sort.Slice(gen.types, func(i, j int) bool { return gen.types[i].name < gen.types[j].name })
	for key := range gen.stringEnums {
		sort.Slice(gen.stringEnums[key], func(i, j int) bool {
			return gen.stringEnums[key][i].value < gen.stringEnums[key][j].value
		})
	}
	actionValues := enumNameValues(gen.stringEnums["ControlAction"])
	for index := range gen.actionSpecs {
		if value := actionValues[gen.actionSpecs[index].action]; value != "" {
			gen.actionSpecs[index].action = value
		}
	}
	sort.Slice(gen.actionSpecs, func(i, j int) bool { return gen.actionSpecs[i].action < gen.actionSpecs[j].action })
	gen.eventFields = loadEventFields()
	return gen, nil
}

func (g *generator) emit() ([]byte, error) {
	var out bytes.Buffer
	out.WriteString("// Code generated by tools/protocolgen. DO NOT EDIT.\n\n")
	for _, decl := range g.types {
		if decl.name == "AstralEvent" {
			g.emitTSAstralEvent(&out)
			continue
		}
		if decl.name == "AstralEventNormalized" {
			g.emitTSAstralEventNormalized(&out)
			continue
		}
		if strings.HasSuffix(decl.name, "Normalized") {
			g.emitTSAstralNormalizedType(&out, decl.name)
			continue
		}
		if values := g.stringEnums[decl.name]; len(values) > 0 {
			parts := make([]string, len(values))
			for index, enum := range values {
				parts[index] = strconv.Quote(enum.value)
			}
			fmt.Fprintf(&out, "export type %s = %s;\n\n", decl.name, strings.Join(parts, " | "))
			continue
		}
		ts, err := g.tsType(decl.expr)
		if err != nil {
			return nil, fmt.Errorf("%s: %w", decl.name, err)
		}
		if fields, ok := ts.(structFields); ok {
			fmt.Fprintf(&out, "export type %s = {\n", decl.name)
			for _, field := range fields {
				optional := ""
				if field.optional {
					optional = "?"
				}
				fmt.Fprintf(&out, "  %s%s: %s;\n", field.name, optional, field.typ)
			}
			out.WriteString("};\n\n")
			continue
		}
		fmt.Fprintf(&out, "export type %s = %s;\n\n", decl.name, ts)
	}
	g.emitTSControlActionMaps(&out)
	return out.Bytes(), nil
}

func (g *generator) eventPayloads() []eventPayload {
	values := g.stringEnums["AstralEventKind"]
	out := make([]eventPayload, 0, len(values))
	for _, value := range values {
		if !strings.HasPrefix(value.name, "AstralEventKind") {
			continue
		}
		payload := strings.TrimPrefix(value.name, "AstralEventKind") + "Normalized"
		out = append(out, eventPayload{kind: value.value, typeName: payload, swiftCase: swiftIdentifier(strings.TrimPrefix(value.name, "AstralEventKind"))})
	}
	sort.Slice(out, func(i, j int) bool { return out[i].kind < out[j].kind })
	return out
}

type eventPayload struct {
	kind      string
	typeName  string
	swiftCase string
}

func loadEventFields() map[string][]string {
	out := map[string][]string{}
	for kind, fields := range protocolmeta.AstralEventNormalizedFieldSpecs() {
		names := make([]string, 0, len(fields))
		seen := map[string]bool{}
		for _, field := range fields {
			if field.Name == "" || seen[field.Name] {
				continue
			}
			seen[field.Name] = true
			names = append(names, field.Name)
		}
		sort.Strings(names)
		out[string(kind)] = names
	}
	return out
}

func (g *generator) payloadFields(typeName string) []string {
	for _, payload := range g.eventPayloads() {
		if payload.typeName == typeName {
			return g.eventFields[payload.kind]
		}
	}
	return nil
}

func (g *generator) emitTSAstralNormalizedType(out *bytes.Buffer, typeName string) {
	fmt.Fprintf(out, "export type %s = {\n", typeName)
	for _, field := range g.payloadFields(typeName) {
		fmt.Fprintf(out, "  %s?: unknown;\n", tsPropertyName(field))
	}
	out.WriteString("  [key: string]: unknown;\n")
	out.WriteString("};\n\n")
}

func (g *generator) emitTSAstralEventNormalized(out *bytes.Buffer) {
	payloads := g.eventPayloads()
	parts := make([]string, 0, len(payloads))
	for _, payload := range payloads {
		parts = append(parts, payload.typeName)
	}
	if len(parts) == 0 {
		out.WriteString("export type AstralEventNormalized = { [key: string]: unknown };\n\n")
		return
	}
	fmt.Fprintf(out, "export type AstralEventNormalized = %s;\n\n", strings.Join(parts, " | "))
}

func (g *generator) emitTSAstralEvent(out *bytes.Buffer) {
	out.WriteString("export type AstralEventBase = {\n")
	out.WriteString("  seq: number;\n")
	out.WriteString("  ts: string;\n")
	out.WriteString("  workspace_id: string;\n")
	out.WriteString("  session_id: string;\n")
	out.WriteString("  agent: AgentKind;\n")
	out.WriteString("  raw?: unknown;\n")
	out.WriteString("};\n\n")
	out.WriteString("export type AstralEvent =\n")
	for _, payload := range g.eventPayloads() {
		fmt.Fprintf(out, "  | (AstralEventBase & { kind: %s; normalized: %s })\n", strconv.Quote(payload.kind), payload.typeName)
	}
	out.WriteString(";\n\n")
}

func (g *generator) emitTSControlActionMaps(out *bytes.Buffer) {
	if len(g.actionSpecs) == 0 {
		return
	}
	out.WriteString("export type ControlActionParamMap = {\n")
	for _, spec := range g.actionSpecs {
		fmt.Fprintf(out, "  %s: %s;\n", strconv.Quote(spec.action), tsSpecType(spec.paramsType, true))
	}
	out.WriteString("};\n\n")
	out.WriteString("export type ControlActionResultMap = {\n")
	for _, spec := range g.actionSpecs {
		fmt.Fprintf(out, "  %s: %s;\n", strconv.Quote(spec.action), tsSpecType(spec.resultType, false))
	}
	out.WriteString("};\n\n")
	out.WriteString("export type ControlActionParams<A extends ControlAction> = ControlActionParamMap[A];\n\n")
	out.WriteString("export type ControlActionResult<A extends ControlAction> = ControlActionResultMap[A];\n\n")
}

func (g *generator) emitSwift() ([]byte, error) {
	var out bytes.Buffer
	out.WriteString("// Code generated by tools/protocolgen. DO NOT EDIT.\n\n")
	out.WriteString("import Foundation\n\n")
	out.WriteString("enum GeneratedProtocol {\n")
	for _, decl := range g.types {
		if decl.name == "AstralEvent" {
			g.emitSwiftAstralEvent(&out)
			continue
		}
		if decl.name == "AstralEventNormalized" {
			continue
		}
		if strings.HasSuffix(decl.name, "Normalized") {
			g.emitSwiftAstralNormalizedStruct(&out, decl.name)
			continue
		}
		if values := g.stringEnums[decl.name]; len(values) > 0 {
			fmt.Fprintf(&out, "  enum %s: String, Codable, Equatable, Sendable {\n", decl.name)
			used := map[string]int{}
			for _, enum := range values {
				caseName := swiftCaseName(decl.name, enum.name, enum.value)
				if count := used[caseName]; count > 0 {
					used[caseName] = count + 1
					caseName = fmt.Sprintf("%s%d", caseName, count+1)
				} else {
					used[caseName] = 1
				}
				fmt.Fprintf(&out, "    case %s = %s\n", caseName, strconv.Quote(enum.value))
			}
			out.WriteString("  }\n\n")
			continue
		}
		fields, ok, err := g.swiftStructFields(decl.expr)
		if err != nil {
			return nil, fmt.Errorf("%s: %w", decl.name, err)
		}
		if !ok {
			alias, err := g.swiftExpr(decl.expr)
			if err != nil {
				return nil, fmt.Errorf("%s: %w", decl.name, err)
			}
			fmt.Fprintf(&out, "  typealias %s = %s\n\n", decl.name, alias)
			continue
		}
		fmt.Fprintf(&out, "  struct %s: Codable, Equatable {\n", decl.name)
		for _, field := range fields {
			typ := field.typ
			if field.optional && !strings.HasSuffix(typ, "?") {
				typ += "?"
			}
			fmt.Fprintf(&out, "    var %s: %s\n", swiftIdentifier(field.name), typ)
		}
		if len(fields) > 0 {
			out.WriteString("\n")
			out.WriteString("    enum CodingKeys: String, CodingKey {\n")
			for _, field := range fields {
				swiftName := swiftIdentifier(field.name)
				if swiftName == field.name {
					fmt.Fprintf(&out, "      case %s\n", swiftName)
				} else {
					fmt.Fprintf(&out, "      case %s = %s\n", swiftName, strconv.Quote(field.name))
				}
			}
			out.WriteString("    }\n")
		}
		out.WriteString("  }\n\n")
	}
	g.emitSwiftAstralNormalizedPayload(&out)
	out.WriteString("}\n")
	return out.Bytes(), nil
}

func (g *generator) emitSwiftAstralNormalizedStruct(out *bytes.Buffer, typeName string) {
	fmt.Fprintf(out, "  struct %s: Codable, Equatable {\n", typeName)
	out.WriteString("    var storage: [String: JSONValue]\n\n")
	out.WriteString("    init(_ value: [String: JSONValue] = [:]) {\n")
	out.WriteString("      self.storage = value\n")
	out.WriteString("    }\n\n")
	out.WriteString("    init(from decoder: Decoder) throws {\n")
	out.WriteString("      storage = try [String: JSONValue](from: decoder)\n")
	out.WriteString("    }\n\n")
	out.WriteString("    func encode(to encoder: Encoder) throws {\n")
	out.WriteString("      try storage.encode(to: encoder)\n")
	out.WriteString("    }\n")
	for _, group := range swiftFieldGroups(g.payloadFields(typeName)) {
		out.WriteString("\n")
		fmt.Fprintf(out, "    var %s: JSONValue? { ", group.name)
		for index, field := range group.fields {
			if index > 0 {
				out.WriteString(" ?? ")
			}
			fmt.Fprintf(out, "storage[%s]", strconv.Quote(field))
		}
		out.WriteString(" }\n")
	}
	out.WriteString("  }\n\n")
}

