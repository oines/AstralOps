	out.WriteString("    var ts: String\n")
	out.WriteString("    var workspaceId: String\n")
	out.WriteString("    var sessionId: String\n")
	out.WriteString("    var agent: AgentKind\n")
	out.WriteString("    var kind: AstralEventKind\n")
	out.WriteString("    var normalized: AstralNormalizedPayload\n")
	out.WriteString("    var raw: JSONValue?\n\n")
	out.WriteString("    enum CodingKeys: String, CodingKey {\n")
	out.WriteString("      case seq\n")
	out.WriteString("      case ts\n")
	out.WriteString("      case workspaceId = \"workspace_id\"\n")
	out.WriteString("      case sessionId = \"session_id\"\n")
	out.WriteString("      case agent\n")
	out.WriteString("      case kind\n")
	out.WriteString("      case normalized\n")
	out.WriteString("      case raw\n")
	out.WriteString("    }\n\n")
	out.WriteString("    init(from decoder: Decoder) throws {\n")
	out.WriteString("      let container = try decoder.container(keyedBy: CodingKeys.self)\n")
	out.WriteString("      seq = try container.decode(Int64.self, forKey: .seq)\n")
	out.WriteString("      ts = try container.decode(String.self, forKey: .ts)\n")
	out.WriteString("      workspaceId = try container.decode(String.self, forKey: .workspaceId)\n")
